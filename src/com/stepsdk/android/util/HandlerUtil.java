package com.stepsdk.android.util;

import android.os.Bundle;
import android.os.Message;

public class HandlerUtil {
    public static Message buildMessage(String key, String value) {
        return HandlerUtil.addToMessage(new Message(), key, value);
    }

    public static Message addToMessage(Message message, String key, String value) {
        if (message == null)
            message = new Message();

        if (message.getData() == null)
            message.setData(new Bundle());

        message.getData().putString(key, value);

        return message;
    }

}
